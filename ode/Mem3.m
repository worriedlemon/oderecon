function dX = Mem3(t,X)
a = -0.667e-3; b = 0.029e-3; p1 = 2.594e-3; p2 = 73.53e3; 
p3 = 147e6;  p6 = 55.55;
p4 = 7.353e3; p5 = 14.7e6;

%initial [0.0 0.11 0.11 0.0]' T = 0.01;
dX = X;

x1 = X(1,:); x2 = X(2,:); x3 = X(3,:); x4 = X(4,:);
dX(1,:) = p1*x2;
dX(2,:) = p2*(x3 - x2) - p3*(a + 3*b*x1.^2).*x2;
dX(3,:) = p4*(x2 - x3) - p5*x4;
dX(4,:) = p6*x3;
 
end